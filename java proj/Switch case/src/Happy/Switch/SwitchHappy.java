package Happy.Switch;

public class SwitchHappy {

	public static void main(String[] args){
		int r= 5;
		switch (r){
		
		case 0:
			System.out.println("Value is 0");
			break;
		case 1:
			System.out.println("Value is 1");
			break;
		case 2:
			System.out.println("Value is 2");
			break;
			default:
				System.out.println("No Value");
				
		}
	}
}
