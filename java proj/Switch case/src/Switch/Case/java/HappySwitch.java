package Switch.Case.java;

public class HappySwitch {

	public static void main(String[] args){
		
		int R = 2;
		
		switch(R){
		
		case 0:
			System.out.println("Value is 0");
			break;
		case 1:
			System.out.println("Value is 1");
			break;
		case 2:
			System.out.println("Value is 2");
			break;
		case 3:
			System.out.println("Value is 3");
			break;
			
		
		}
	}
}
