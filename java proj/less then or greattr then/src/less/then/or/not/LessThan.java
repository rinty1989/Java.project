package less.then.or.not;

public class LessThan {
public static void main (String[] args){
	
	
	int app = 20;
	int jaa = 20;
	
	if (app < jaa){
		System.out.println("app is less than jaa");
	}
	if (app == jaa){
		System.out.println("app is equal to jaa");
	}
	if (app > jaa){
		System.out.println("app is greater than jaa");
	}
}
}
